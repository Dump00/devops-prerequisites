# Numeric data types

# int
num1 = 5
#float
num2 = 2.0
#complex
num3 = 1+2j
print(num1, 'is type of', type(num1))
print(num2, 'is type of', type(num2))
print(num3, 'is type of', type(num3))


# String
name = 'Python'
print(name)


# List
languages = ["Java", "Python", "Go"]
print(languages)
print(languages[0])


# Tuple (tuples are immutable)
product = ('Microsoft', 'XBOX', 499.99)
print(product[1])


#  Set 
student_id = {1, 2, 3, 4, 4, 5}
print(student_id)

# Dictionary
capitals = {
    'Italy': 'Rome',
    'Australia': 'Melbourne',
    'UK': 'London' 
}
print(capitals)
print(capitals['UK'])





